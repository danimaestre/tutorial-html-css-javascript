function abrirVentana() {
    let ventana = window.open("", "NuevaVentana", "width=500,height=700");
    ventana.document.write("Esta es una nueva ventana<br>");
    ventana.document.write("<html><head><title>Esta ventana es un pop</title></head>");
    ventana.document.write("<body><img src='img/casa.jpg' alt='casa'></body></html>");
    ventana.moveTo(700, 100);
    ventana.document.body.style.backgroundColor = 'white';
    ventana.document.body.style.color = 'white';
}

